#!/usr/bin/env node
// spec-v590 §Budget-note remediation: per-group alias shard split.
//
// data/search/aliases.json (the authored master, still the single source
// of truth for authoring, gates, tests, and the MCP server) crossed the
// ~250 KB-gzipped threshold spec-v590 set as the split trigger (261.9 KB
// on 2026-07-17). This script derives one runtime shard per tile group --
// data/search/aliases-<letter>.json -- by keying each alias row on its
// target tile's group in tools-data.js. app.js fetches the per-group
// shards in parallel on first search interaction and folds each one in
// as it arrives, so alias search becomes usable progressively instead of
// waiting on one monolithic fetch; sw.js precaches the shards (not the
// master). The master is never fetched by the runtime.
//
// Also rewrites the alias entries of data/search/manifest.json (per-shard
// gzip sizes, "pending" hashes, master size kept current) so growth stays
// visible in review, and deletes stale aliases-*.json for retired groups.
//
// Flags:
//   --check    Regenerate in memory and compare against the on-disk
//              shards + manifest, and assert sw.js DATA_MANIFESTS lists
//              exactly the shard set (and not the master). Exit non-zero
//              on any drift. Wired into `npm run lint`.
//   (default)  Write the shards and manifest in place.
//
// Deterministic: output depends only on aliases.json + tools-data.js.
// Pure read-and-write; no network, no third-party imports.

import { readFile, writeFile, readdir, unlink } from "node:fs/promises";
import { resolve, dirname } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { gzipSync } from "node:zlib";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const SEARCH_DIR = resolve(ROOT, "data", "search");
const MASTER = resolve(SEARCH_DIR, "aliases.json");
const MANIFEST = resolve(SEARCH_DIR, "manifest.json");
const SW = resolve(ROOT, "sw.js");

const CHECK = process.argv.includes("--check");

function fail(msg) {
  console.error("build-alias-shards: " + msg);
  process.exit(1);
}

function gzipSize(text) {
  return gzipSync(Buffer.from(text, "utf8"), { level: 9 }).length;
}

// Deterministic view of the manifest for the --check comparison: every field
// except the per-shard gzip_size_bytes (which varies a few bytes across zlib
// builds and is informational only). Catches added / removed / renamed / mis-
// ordered shards and hash-key drift without gating on a non-portable number.
function manifestShape(m) {
  return JSON.stringify({
    ...m,
    shards: (m.shards || []).map((s) => ({ file: s.file, name: s.name })),
  });
}

// Serialize a shard with one alias row per line so diffs stay per-row,
// matching the review ergonomics of the master file.
function serializeShard(letter, updated, rows) {
  const head =
    "{\n" +
    '  "_comment": "Generated by scripts/build-alias-shards.mjs from data/search/aliases.json (the authoring master). Runtime shard for group ' +
    letter +
    ": app.js ensureAliases fetches every group's shard in parallel on first search interaction. Do not edit by hand; edit the master and rerun the script.\",\n" +
    '  "_updated": ' + JSON.stringify(updated) + ",\n" +
    '  "aliases": [';
  const body = rows.map((r) => "\n    " + JSON.stringify(r)).join(",");
  return head + body + "\n  ]\n}\n";
}

async function main() {
  const masterRaw = await readFile(MASTER, "utf8");
  const master = JSON.parse(masterRaw);
  if (!master || !Array.isArray(master.aliases)) {
    fail("data/search/aliases.json has no aliases array.");
  }

  const { TOOLS } = await import(pathToFileURL(resolve(ROOT, "tools-data.js")).href);
  const groupOf = new Map(TOOLS.map((t) => [t.id, t.group]));
  const letters = [...new Set(TOOLS.map((t) => t.group))].sort();

  // Bucket rows by the target tile's group, preserving master order.
  const buckets = new Map(letters.map((l) => [l, []]));
  for (const row of master.aliases) {
    const g = groupOf.get(row.target);
    if (!g) {
      // check-discoverability guarantees live targets; a miss here means
      // the shards were regenerated against a stale tools-data.js.
      fail("alias target '" + row.target + "' is not a live tile id in tools-data.js.");
    }
    buckets.get(g).push(row);
  }

  const updated = typeof master._updated === "string" ? master._updated : "";
  const expected = new Map(); // filename -> content
  for (const l of letters) {
    expected.set("aliases-" + l.toLowerCase() + ".json", serializeShard(l, updated, buckets.get(l)));
  }

  // Rewrite the manifest's alias entries: master size kept current, one
  // entry per group shard, hashes "pending" per the search-data convention.
  const manifest = JSON.parse(await readFile(MANIFEST, "utf8"));
  const keptShards = (manifest.shards || []).filter(
    (s) => s.file !== "aliases.json" && !/^aliases-[a-z]\.json$/.test(s.file)
  );
  const keptHashes = {};
  for (const [k, v] of Object.entries(manifest.hashes || {})) {
    if (k !== "aliases.json" && !/^aliases-[a-z]\.json$/.test(k)) keptHashes[k] = v;
  }
  const aliasShardEntries = letters.map((l) => {
    const file = "aliases-" + l.toLowerCase() + ".json";
    return {
      file,
      name: "Search aliases, group " + l + " runtime shard (generated from aliases.json)",
      gzip_size_bytes: gzipSize(expected.get(file)),
    };
  });
  manifest.shards = [
    {
      file: "aliases.json",
      name: "Search aliases master (authoring source; runtime loads the per-group aliases-<letter>.json shards)",
      gzip_size_bytes: gzipSize(masterRaw),
    },
    ...aliasShardEntries,
    ...keptShards,
  ];
  manifest.hashes = {
    "aliases.json": (manifest.hashes || {})["aliases.json"] || "pending",
    ...Object.fromEntries(aliasShardEntries.map((s) => [s.file, "pending"])),
    ...keptHashes,
  };
  const manifestOut = JSON.stringify(manifest, null, 2) + "\n";

  // The sw.js DATA_MANIFESTS list must precache exactly the shard set --
  // and not the master, which would double the install payload.
  const swSource = await readFile(SW, "utf8");
  const dmMatch = swSource.match(/const\s+DATA_MANIFESTS\s*=\s*\[([\s\S]*?)\]\s*;/);
  if (!dmMatch) fail("could not extract DATA_MANIFESTS from sw.js");
  const dmEntries = [...dmMatch[1].matchAll(/["']([^"']+)["']/g)].map((m) => m[1]);
  const swAlias = new Set(dmEntries.filter((e) => /\/aliases[.-]/.test(e)));
  const swErrors = [];
  for (const file of expected.keys()) {
    if (!swAlias.has("./data/search/" + file)) {
      swErrors.push("sw.js DATA_MANIFESTS is missing './data/search/" + file + "'.");
    }
  }
  for (const e of swAlias) {
    if (e === "./data/search/aliases.json") {
      swErrors.push("sw.js DATA_MANIFESTS must not precache the master aliases.json (runtime loads the per-group shards).");
    } else if (!expected.has(e.replace("./data/search/", ""))) {
      swErrors.push("sw.js DATA_MANIFESTS lists stale alias shard '" + e + "'.");
    }
  }

  // Stale shard files for retired groups.
  const onDisk = (await readdir(SEARCH_DIR)).filter((n) => /^aliases-[a-z]\.json$/.test(n));
  const stale = onDisk.filter((n) => !expected.has(n));

  if (CHECK) {
    const errors = [...swErrors];
    for (const [file, content] of expected) {
      let current = null;
      try {
        current = await readFile(resolve(SEARCH_DIR, file), "utf8");
      } catch { /* missing counts as drift */ }
      if (current !== content) errors.push("data/search/" + file + " is stale or missing.");
    }
    for (const n of stale) errors.push("data/search/" + n + " is stale (its group no longer exists).");
    // Compare manifest STRUCTURE, not bytes: gzip_size_bytes is informational
    // and not reproducible across zlib builds (macOS vs CI Linux differ by a
    // few bytes), so gating on it would fail CI on an otherwise-correct commit.
    // The shard file list, names, order, and hash keys ARE deterministic.
    const manifestCurrent = JSON.parse(await readFile(MANIFEST, "utf8"));
    if (manifestShape(manifestCurrent) !== manifestShape(manifest)) {
      errors.push("data/search/manifest.json alias shard list / hashes are stale (run the generator).");
    }
    if (errors.length > 0) {
      for (const e of errors) console.error("ERROR: " + e);
      console.error(
        "alias shards out of sync with aliases.json/tools-data.js; run `node scripts/build-alias-shards.mjs` (and update sw.js if it names the drift). " +
        errors.length + " errors."
      );
      process.exit(1);
    }
    console.log(
      "alias shards OK (" + letters.length + " group shards in sync with the master, manifest current, sw.js precache list exact)."
    );
    return;
  }

  if (swErrors.length > 0) {
    // Write mode still emits the data files, but a wrong precache list is
    // a hand-edit the script must not paper over silently.
    for (const e of swErrors) console.error("ERROR: " + e);
    console.error("update sw.js DATA_MANIFESTS to list exactly the generated './data/search/aliases-<letter>.json' set.");
    process.exitCode = 1;
  }
  let wrote = 0;
  for (const [file, content] of expected) {
    let current = null;
    try {
      current = await readFile(resolve(SEARCH_DIR, file), "utf8");
    } catch { /* new file */ }
    if (current !== content) {
      await writeFile(resolve(SEARCH_DIR, file), content);
      wrote++;
    }
  }
  for (const n of stale) {
    await unlink(resolve(SEARCH_DIR, n));
    console.log("removed stale data/search/" + n);
  }
  const manifestCurrent = await readFile(MANIFEST, "utf8");
  if (manifestCurrent !== manifestOut) {
    await writeFile(MANIFEST, manifestOut);
    wrote++;
  }
  console.log(
    wrote === 0
      ? "alias shards already current. No changes written."
      : "wrote " + wrote + " file(s): " + letters.length + " group shards + manifest for " + master.aliases.length + " alias rows."
  );
}

await main();
