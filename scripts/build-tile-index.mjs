#!/usr/bin/env node
// spec-v14 §13.1 Phase I per-tile derivation index.
//
// Walks the TOOLS array in app.js and the worked-examples fixture
// registry, then emits a deterministic per-tile index section into
// docs/derivations.md. The index names every TOOLS tile_id with its
// group, the source publisher / section that the v10 cross-validation
// fixture cites, and a pointer to the fixture file row. Per spec-v14
// §13.1 every tile in the TOOLS array carries a derivation row that
// names the formula, the citation, and the worked-example fixture;
// this section is the per-tile half of that contract (the per-function
// half is the existing "Function corpus (v14)" generated table at
// `scripts/build-corpus.mjs`).
//
// Idempotent. Same flag pattern as build-corpus.mjs:
//   --check    Compare regenerated section against the on-disk file
//              and exit non-zero on any diff. Wired into `npm run
//              audit:tile-index` and `npm run lint`.
//   (default)  Rewrite the section in place.
//
// Pure read-and-write; no network, no third-party imports.

import { readFile, writeFile } from "node:fs/promises";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const DERIVATIONS = resolve(ROOT, "docs", "derivations.md");
const APP_JS = resolve(ROOT, "app.js");
const FIXTURES = resolve(ROOT, "test", "fixtures", "worked-examples.json");

const SECTION_START = "<!-- BEGIN tile-index-v14 (generated by scripts/build-tile-index.mjs) -->";
const SECTION_END = "<!-- END tile-index-v14 -->";

// Per spec-v14 §12.1 the H References and Q Historical groups are
// reference-only; their tiles use the v6 source-stamp recheck cadence
// instead of a per-formula derivation. They appear in the index for
// completeness with the v6-audit pointer as their "fixture" column.
const REFERENCE_GROUPS = new Set(["H", "Q"]);

const GROUP_NAMES = {
  A: "Electrical",
  B: "Plumbing",
  C: "HVAC",
  D: "Restoration",
  E: "Construction",
  F: "Fire-ground",
  G: "Cross-trade",
  H: "References",
  J: "Trucking",
  K: "Mechanic",
  L: "Agriculture",
  M: "Water and wastewater",
  N: "Stage",
  O: "Kitchen",
  P: "Field",
  Q: "Historical",
  R: "Accounting",
  S: "Legal",
  T: "Lab",
  U: "Veterinary",
  V: "EMS",
  W: "Aviation",
  X: "Real Estate",
  Y: "Educators",
};

async function loadTools() {
  const text = await readFile(APP_JS, "utf8");
  const out = [];
  // Match `{ id: "tile-id", name: "Display", group: "X", ... }` in TOOLS.
  // The pattern uses lazy capture between id and group to tolerate any
  // intermediate fields (name / trades / desc / etc.) in any order; in
  // practice id is always followed shortly by name then group.
  const re = /\{\s*id:\s*"([a-z0-9-]+)"\s*,\s*name:\s*"([^"]+)"\s*,\s*group:\s*"([A-Z])"/g;
  let m;
  while ((m = re.exec(text)) !== null) {
    out.push({ id: m[1], name: m[2], group: m[3] });
  }
  return out;
}

async function loadFixtures() {
  const j = JSON.parse(await readFile(FIXTURES, "utf8"));
  // Group rows by tile_id. A tile may have multiple fixture rows; the
  // index lists the first row's source as the canonical reference and
  // notes the additional-row count where it exceeds one.
  const map = new Map();
  for (const r of j.rows) {
    if (!map.has(r.tile_id)) map.set(r.tile_id, []);
    map.get(r.tile_id).push(r);
  }
  return map;
}

function escapePipes(s) {
  return String(s).replace(/\|/g, "\\|");
}

function shorten(s, max = 60) {
  const t = String(s || "").replace(/\s+/g, " ").trim();
  return t.length > max ? t.slice(0, max - 3) + "..." : t;
}

function citationCell(fixtures) {
  if (!fixtures || fixtures.length === 0) return "_";
  const first = fixtures[0];
  const pub = shorten(first.source_publisher || "_", 40);
  const sec = shorten(first.source_section_or_page || first.source_title || "_", 60);
  return `${pub}; ${sec}`;
}

function fixtureCell(tile_id, fixtures, group) {
  if (REFERENCE_GROUPS.has(group)) {
    return "[docs/v6-audit.md](v6-audit.md) (reference cadence)";
  }
  if (!fixtures || fixtures.length === 0) return "_";
  const n = fixtures.length;
  const suffix = n > 1 ? ` (+${n - 1} more)` : "";
  return `[test/fixtures/worked-examples.json](../test/fixtures/worked-examples.json)${suffix}`;
}

async function buildSection() {
  const tools = await loadTools();
  const fixtures = await loadFixtures();

  // Stable sort: group letter, then tile_id.
  tools.sort((a, b) =>
    a.group < b.group ? -1 : a.group > b.group ? 1
    : a.id < b.id ? -1 : a.id > b.id ? 1 : 0,
  );

  const lines = [];
  lines.push(SECTION_START);
  lines.push("");
  lines.push("## Per-tile derivation index (v14)");
  lines.push("");
  lines.push("Per spec-v14 §13.1, one row per TOOLS tile recording the tile id,");
  lines.push("its group, the source publisher / section that the v10 cross-");
  lines.push("validation fixture cites for the tile's worked example, and a");
  lines.push("pointer to the fixture file. This table is the mechanically-");
  lines.push("extracted skeleton emitted by");
  lines.push("[scripts/build-tile-index.mjs](../scripts/build-tile-index.mjs)");
  lines.push("and kept in sync by `npm run audit:tile-index` (wired into");
  lines.push("`npm run lint`).");
  lines.push("");
  lines.push("The per-formula derivation prose lives in the numbered sections");
  lines.push("above; one section per formula family covers the tiles that share");
  lines.push("the underlying physics. Reference-only tiles (Groups H and Q per");
  lines.push("spec-v14 §12.1) record the v6 source-stamp recheck row in");
  lines.push("[docs/v6-audit.md](v6-audit.md) rather than a formula derivation,");
  lines.push("per spec-v14 §13.1 second paragraph.");
  lines.push("");

  // One subsection per group, then a flat table inside.
  const byGroup = new Map();
  for (const t of tools) {
    if (!byGroup.has(t.group)) byGroup.set(t.group, []);
    byGroup.get(t.group).push(t);
  }
  const groupLetters = [...byGroup.keys()].sort();

  for (const g of groupLetters) {
    const rows = byGroup.get(g);
    const groupLabel = GROUP_NAMES[g] || "(unnamed)";
    lines.push(`### Group ${g} ${groupLabel} (${rows.length} tile${rows.length === 1 ? "" : "s"})`);
    lines.push("");
    lines.push("| tile_id | name | citation source | fixture |");
    lines.push("| --- | --- | --- | --- |");
    for (const t of rows) {
      const fx = fixtures.get(t.id) || [];
      lines.push(
        `| \`${t.id}\` | ${escapePipes(t.name)} | ${escapePipes(citationCell(fx))} | ${fixtureCell(t.id, fx, t.group)} |`,
      );
    }
    lines.push("");
  }

  const totalCovered = tools.filter((t) => fixtures.has(t.id) || REFERENCE_GROUPS.has(t.group)).length;
  lines.push(
    `Tile count: ${tools.length}. Fixture-covered or reference-cadence: ${totalCovered} / ${tools.length}.`,
  );
  lines.push("");
  lines.push(SECTION_END);
  return lines.join("\n") + "\n";
}

function spliceSection(existing, regenerated) {
  const startIdx = existing.indexOf(SECTION_START);
  const endIdx = existing.indexOf(SECTION_END);
  if (startIdx !== -1 && endIdx !== -1) {
    const before = existing.slice(0, startIdx);
    const after = existing.slice(endIdx + SECTION_END.length);
    const trailingNl = after.startsWith("\n") ? "" : "\n";
    return before + regenerated.trimEnd() + trailingNl + after;
  }
  const sep = existing.endsWith("\n") ? "" : "\n";
  return existing + sep + "\n" + regenerated;
}

async function main() {
  const check = process.argv.includes("--check");
  const regenerated = await buildSection();
  const existing = await readFile(DERIVATIONS, "utf8");
  const next = spliceSection(existing, regenerated);
  if (check) {
    if (next !== existing) {
      console.error(
        "FAIL: docs/derivations.md tile-index section is stale.\n" +
          "      Run `npm run audit:tile-index` and commit the diff.",
      );
      process.exit(1);
    }
    console.log("v14 tile-index lint OK.");
    return;
  }
  if (next === existing) {
    console.log("v14 tile-index already current. No changes written.");
    return;
  }
  await writeFile(DERIVATIONS, next);
  console.log("v14 tile-index regenerated.");
}

main().catch((err) => {
  console.error(err.stack || err.message);
  process.exit(1);
});
